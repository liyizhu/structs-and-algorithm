package com.algorithm.hash.question1;

/*
链表结点
key：该结点的值
next：该结点的下一个结点
 */
public class ListNode {
    private int key;
    private ListNode next;

    public int getKey() {
        return key;
    }

    public void setKey(int key) {
        this.key = key;
    }

    public ListNode getNext() {
        return next;
    }

    public void setNext(ListNode next) {
        this.next = next;
    }
}
